#include <stdio.h>
int main()
{
    // int a; Declaration Of variable in C
    // a = 10; Initialization of variable in C

    int a = 10; // Declaration and Initialization of variable in C

    printf("%d\n",a);//Print the value of a

    char c = 'a';
    printf("%c\n",c);

    float f = 10.5;
    printf("%f\n",f);

    double d = 10.5;
    printf("%lf\n",d);


    // Print the size of any datatype
    printf("%d\n",sizeof(d));

    

    return 0;
}